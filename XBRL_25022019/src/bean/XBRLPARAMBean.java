package bean;

public class XBRLPARAMBean {
	
	
	
	private String refCode;	
	private String refCodes;
	private String refCodedisc;
	private String sourceSyscode;
	
	
	
	
	
	
	public String getRefCode() {
		return refCode;
	}
	public void setRefCode(String refCode) {
		this.refCode = refCode;
	}
	public String getRefCodes() {
		return refCodes;
	}
	public void setRefCodes(String refCodes) {
		this.refCodes = refCodes;
	}
	public String getRefCodedisc() {
		return refCodedisc;
	}
	public void setRefCodedisc(String refCodedisc) {
		this.refCodedisc = refCodedisc;
	}
	public String getSourceSyscode() {
		return sourceSyscode;
	}
	public void setSourceSyscode(String sourceSyscode) {
		this.sourceSyscode = sourceSyscode;
	}
	
	

}
